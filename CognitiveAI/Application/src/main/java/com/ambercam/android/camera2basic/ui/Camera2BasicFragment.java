    /**
     * Saves a JPEG {@link Image} into the specified {@link File}.
     */
    private static class ImageSaver implements Runnable {

        /**
         * The JPEG image
         */
        private final Image mImage;
        /**
         * The file we save the image into.
         */
        private final File mFile;

        //The variable used to access firebase storage
        final String FIREBASE_BUCKET = "gs://cameraapp-6a5ab.appspot.com";//"gs://cloudcamera-95ade.appspot.com";

        //Firebase variables
        private FirebaseDatabase mDatabase;
        private FirebaseStorage mStorage;
        private FirebaseUser mUser;

        private Context mContext;

        private String[] mSplitUser;
        private String mTimestamp;
        private CountData mCountData;

        private MaterialDialog mProgressBuilder;

        public ImageSaver(Image image,
                          Context context,
                          File file,
                          FirebaseStorage storage,
                          FirebaseUser user,
                          FirebaseDatabase database,
                          CountData countData) {
            mImage = image;
            mContext = context;
            mFile = file;
            mStorage = storage;
            mUser = user;
            mDatabase = database;
            mCountData = countData;
        }

        @Override
        public void run() {

            ByteBuffer buffer = mImage.getPlanes()[0].getBuffer();
            byte[] bytes = new byte[buffer.remaining()];
            buffer.get(bytes);
            FileOutputStream output = null;

//            mTimestamp = String.valueOf(mImage.getTimestamp());

            mTimestamp = String.valueOf(UID);
            try {
                output = new FileOutputStream(mFile);
                output.write(bytes);

                //removes @email.com from user email
                mSplitUser = mUser.getEmail().split("@");

                storeImageInFirebaseStorage(mSplitUser[0], mImage);

            } catch (IOException e) {
                e.printStackTrace();
            } finally {
                mImage.close();
                if (null != output) {
                    try {
                        output.close();
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                }
            }
        }

        /**
         * stores the image from the camera in firebase storage
         */
        public void storeImageInFirebaseStorage(String user, Image image){

            mProgressBuilder = Util.showProgressDialog(mContext);

            //location of temporary image storage
            final String fileLocal = "/storage/emulated/0/Android/data/com.ambercam.android.camera2basic/files/pic.jpg";

            Uri uri = Uri.fromFile(new File(fileLocal));

            //reference to project wide storage
            StorageReference storageReference = mStorage.getReferenceFromUrl(FIREBASE_BUCKET);

            //reference to user unique storage
            StorageReference userReference = storageReference.child(user);

            //TODO: add id to images

            //reference to value labeled by timestamp within user unique reference
            StorageReference imageReference = userReference.child(String.valueOf(image.getTimestamp()));

            UploadTask uploadTask = imageReference.putFile(uri);
            uploadTask.addOnFailureListener(new OnFailureListener() {
                @Override
                public void onFailure(@NonNull Exception e) {
                    mProgressBuilder.dismiss();
                    Log.i("Upload Status: ", "Failed");
                }
            }).addOnSuccessListener(new OnSuccessListener<UploadTask.TaskSnapshot>() {
                @Override
                public void onSuccess(UploadTask.TaskSnapshot taskSnapshot) {

                    Uri downloadUri = taskSnapshot.getDownloadUrl();
                    Log.i("Upload Status: ", "Success");

                    storeImageDataInRealtimeDb(mDatabase, mSplitUser[0], downloadUri.toString());

                    mCountData.setImageCount(mCountData.getImageCount()+1);

                    updateImageCount(mDatabase, Util.returnSplitEmail(mUser.getEmail()));

                    mProgressBuilder.dismiss();
                }
            });
        }

        /**
         *  saves download url to the firebase database
         */
        public void storeImageDataInRealtimeDb(FirebaseDatabase database,
                                               String user,
                                               String url){

            CloudImage cloudImage = new CloudImage(url, mTimestamp);

            //reference to the main tree
            DatabaseReference reference = database.getReference(user);

            //reference to the user unique tree within the main tree
            DatabaseReference subReference = reference.child(mTimestamp);
            reference.setValue(cloudImage);
            //sets a new value containing the url within the user unique tree
//            subReference.setValue(cloudImage);
            UID++;
        }

        public void updateImageCount(FirebaseDatabase database,
                                     String user){

            //reference to the users image count
            DatabaseReference reference = database.getReference(user + "_count");

            //reference to the users count data
            DatabaseReference subReference = reference.child("countData");

            subReference.setValue(mCountData);
        }
    }

    /**
     * Compares two {@code Size}s based on their areas.
     */
    static class CompareSizesByArea implements Comparator<Size> {

        @Override
        public int compare(Size lhs, Size rhs) {
            // We cast here to ensure the multiplications won't overflow
            return Long.signum((long) lhs.getWidth() * lhs.getHeight() -
                    (long) rhs.getWidth() * rhs.getHeight());
        }
    }

